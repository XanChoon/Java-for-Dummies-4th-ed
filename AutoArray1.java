class AutoArray1{
    public static void main (String [] args){
        int age[] = {12, 22, 26};
        System.out.println("Ali age is: " + age [0] + " years old ");
        System.out.println("Abu age is: " + age [1] + " years old ");
        System.out.println("Ahmad age is: " + age [2] + " years old ");
    }
}